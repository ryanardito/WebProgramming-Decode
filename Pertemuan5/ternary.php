<?php 

	// ternary, shorthand (shortcut) dari if else
	// fungsinya, seperti "if else" namun satu baris

	$angka = 10;
	
	// metode biasa 
	if ($angka > 9) {
		echo "ok!";
	}

	else
	{
		echo "gak!";
	}
	

	// metode ternary
	echo $angka > 9 ? "ok!" : "gak!";
















 ?>